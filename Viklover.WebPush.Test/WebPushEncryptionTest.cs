    [Description("Зашифровать и расшифровать сообщение")]
    public async Task EncryptAndDecryptMessageTest(string message) {
        using var userKeyPair = ECDiffieHellman.Create(ECCurve.NamedCurves.nistP256);
        var userKeyParams = userKeyPair.ExportParameters(true);
        var userPublicKey = ComputePublicKey(userKeyParams);
        await PrintArrayAsync(userPublicKey);
        var userPrivateKey = userKeyParams.D!;
        await PrintArrayAsync(userPrivateKey);
        var userSecret = WebPushEncryptor.GenerateSalt(20);
        await PrintArrayAsync(userSecret);
        var payload = Encoding.UTF8.GetBytes(message);
        var encryptionResult = WebPushEncryptor.Encrypt(userPublicKey, userSecret, payload);
        await Console.Out.WriteLineAsync("Encryption result (salt, serverPubKey, encrypted payload):");
        await PrintArrayAsync(encryptionResult.Salt);
        await PrintArrayAsync(encryptionResult.PublicKey);
        await PrintArrayAsync(encryptionResult.Payload);
        var decryptedPayload = WebPushEncryptor.Decrypt(
            encryptionResult.Payload,
            encryptionResult.Salt,
            encryptionResult.PublicKey,
            userPublicKey,
            userPrivateKey,
            userSecret
        );
        await PrintArrayAsync(decryptedPayload);
        var decryptedPayloadText = Encoding.UTF8.GetString(decryptedPayload);
        await Console.Out.WriteLineAsync(decryptedPayloadText);
        Assert.That(decryptedPayload, Is.EqualTo(decryptedPayloadText));
    }
